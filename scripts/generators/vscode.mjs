      "titleBar.activeForeground": hex(u.fg),
      "titleBar.inactiveBackground": hex(u.bgAlt),
      "statusBar.background": hex(u.bgAlt),
      "statusBar.foreground": hex(u.fgDim),
      "statusBar.border": hex(u.border),
