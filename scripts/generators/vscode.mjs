      "editorBracketMatch.background": hex(u.bgHl),
      "editorBracketMatch.border": hex(u.accent),
      // gutter / widgets
      "editorWidget.background": hex(u.bgAlt),
      "editorWidget.border": hex(u.border),
      "editorHoverWidget.background": hex(u.bgAlt),
      "editorHoverWidget.border": hex(u.border),
      "editorSuggestWidget.background": hex(u.bgAlt),
      "editorSuggestWidget.selectedBackground": hex(u.bgHl),
      // workbench chrome
      "activityBar.background": hex(u.bgAlt),
      "activityBar.foreground": hex(u.fg),
      "activityBar.inactiveForeground": hex(u.comment),
      "activityBarBadge.background": hex(u.accent),
      "activityBarBadge.foreground": "#ffffff",
      "sideBar.background": hex(u.bgAlt),
      "sideBar.foreground": hex(u.fgDim),
      "sideBar.border": hex(u.border),
      "sideBarSectionHeader.background": hex(u.bg),
      "sideBarTitle.foreground": hex(u.fg),
      "list.activeSelectionBackground": hex(u.bgHl),
      "list.activeSelectionForeground": hex(u.fg),
      "list.hoverBackground": hex(u.bgHl),
      "list.highlightForeground": hex(u.accent),
      "tab.activeBackground": hex(u.bg),
      "tab.inactiveBackground": hex(u.bgAlt),
      "tab.activeForeground": hex(u.fg),
